// Find the perimeter of this values length = 4 and breadth = 2. 
// length = 7 and breadth = 3.


void main() {

int length = 4;
int breadth = 2;

int perimeter = 2 * (length + breadth);
print('The first perimeter is: $perimeter');

int l = 7;
int b = 3;

int perimeter2 = 2 * (l + b);
print('The second perimeter is: $perimeter2');

}